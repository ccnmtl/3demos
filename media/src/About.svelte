<article>
    <p>
        Welcome to 3Demos. This is a tool dedicated to visualizations
        for multivariable calculus. There are two ways in which one
        can interact with the site, what your might call "story mode"
        and "creative mode".
    </p>

    <p>
        In story mode, the user can select a topic from the "ToC" menu
        above. A mix of theory, examples, and exercises are presented
        along with relevant graphical objects. Some interactive
        elements are embedded in the text.
    </p>

    <p>
        Objects are found in the Object List <i class="fa fa-sliders"
        /> accessible from the button in the upper-right. Here, one
        can create (and destroy) as many of the objects as one
        pleases. Note that hardware limitations may affect
        performance.
    </p>

